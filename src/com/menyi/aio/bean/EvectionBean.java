package com.menyi.aio.bean;

import java.io.Serializable;

/**
 * 出差信息类
 * @author Administrator
 *
 */
public class EvectionBean implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	private String evectionNo;
	private String employeeNo;
	private String createDate;
	private String evectionStartDate;
	private String evectionReturnDate;
	
	public EvectionBean() {}
	
	public EvectionBean(String evectionNo,String employeeNo,String createDate,
			String evectionStartDate,String evectionReturnDate){
		this.evectionNo = evectionNo;
		this.employeeNo = employeeNo;
		this.createDate = createDate;
		this.evectionStartDate = evectionStartDate;
		this.evectionReturnDate = evectionReturnDate;
	}

	public static long getSerialVersionUID() {
		return serialVersionUID;
	}

	public String getCreateDate() {
		return createDate;
	}

	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}

	public String getEmployeeNo() {
		return employeeNo;
	}

	public void setEmployeeNo(String employeeNo) {
		this.employeeNo = employeeNo;
	}

	public String getEvectionNo() {
		return evectionNo;
	}

	public void setEvectionNo(String evectionNo) {
		this.evectionNo = evectionNo;
	}

	public String getEvectionReturnDate() {
		return evectionReturnDate;
	}

	public void setEvectionReturnDate(String evectionReturnDate) {
		this.evectionReturnDate = evectionReturnDate;
	}

	public String getEvectionStartDate() {
		return evectionStartDate;
	}

	public void setEvectionStartDate(String evectionStartDate) {
		this.evectionStartDate = evectionStartDate;
	}
	
	
}
