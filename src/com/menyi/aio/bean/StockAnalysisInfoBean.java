package com.menyi.aio.bean;

public class StockAnalysisInfoBean {
	private double upperLimitProportion;
	private double lowerLimitProportion;
	private int resort;
	private int frequency;
	private String lastTime;
	private String nextTime;
	public int getFrequency() {
		return frequency;
	}
	public void setFrequency(int frequency) {
		this.frequency = frequency;
	}
	public String getLastTime() {
		return lastTime;
	}
	public void setLastTime(String lastTime) {
		this.lastTime = lastTime;
	}
	public double getLowerLimitProportion() {
		return lowerLimitProportion;
	}
	public void setLowerLimitProportion(double lowerLimitProportion) {
		this.lowerLimitProportion = lowerLimitProportion;
	}
	public String getNextTime() {
		return nextTime;
	}
	public void setNextTime(String nextTime) {
		this.nextTime = nextTime;
	}
	public int getResort() {
		return resort;
	}
	public void setResort(int resort) {
		this.resort = resort;
	}
	public double getUpperLimitProportion() {
		return upperLimitProportion;
	}
	public void setUpperLimitProportion(double upperLimitProportion) {
		this.upperLimitProportion = upperLimitProportion;
	}

}
