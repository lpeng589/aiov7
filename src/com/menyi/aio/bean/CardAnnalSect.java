package com.menyi.aio.bean;

import java.io.Serializable;

public class CardAnnalSect implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private String onDutyCard;
	private String offDutyCard;
	private String dutyDate;
	
	public CardAnnalSect() {}
	
	public CardAnnalSect(String onDutyCard,String offDutyCard,String dutyDate) {
		this.onDutyCard = onDutyCard;
		this.offDutyCard = offDutyCard;
		this.dutyDate = dutyDate;
	}
	
	public static long getSerialVersionUID() {
		return serialVersionUID;
	}
	public String getOffDutyCard() {
		return offDutyCard;
	}
	public void setOffDutyCard(String offDutyCard) {
		this.offDutyCard = offDutyCard;
	}
	public String getOnDutyCard() {
		return onDutyCard;
	}
	public void setOnDutyCard(String onDutyCard) {
		this.onDutyCard = onDutyCard;
	}

	public String getDutyDate() {
		return dutyDate;
	}

	public void setDutyDate(String dutyDate) {
		this.dutyDate = dutyDate;
	}
	
	

}
