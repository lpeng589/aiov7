package com.menyi.aio.bean;

import java.io.Serializable;

public class LeaveBean implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String leaveNo;
	private String employeeNo;
	private String createDate;
	private String leaveStartDate;
	private String leaveReturnDate;
	private String leavebillType;
	
	public LeaveBean() {}
	
	public LeaveBean(String leaveNo,String employeeNo,String createDate,
			String leaveStartDate,String leaveReturnDate,String leavebillType){
		this.leaveNo = leaveNo;
		this.employeeNo = employeeNo;
		this.createDate = createDate;
		this.leaveStartDate = leaveStartDate;
		this.leaveReturnDate = leaveReturnDate;
		this.leavebillType = leavebillType;
	}

	public static long getSerialVersionUID() {
		return serialVersionUID;
	}

	public String getCreateDate() {
		return createDate;
	}

	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}

	public String getEmployeeNo() {
		return employeeNo;
	}

	public void setEmployeeNo(String employeeNo) {
		this.employeeNo = employeeNo;
	}

	public String getLeavebillType() {
		return leavebillType;
	}

	public void setLeavebillType(String leavebillType) {
		this.leavebillType = leavebillType;
	}

	public String getLeaveNo() {
		return leaveNo;
	}

	public void setLeaveNo(String leaveNo) {
		this.leaveNo = leaveNo;
	}

	public String getLeaveReturnDate() {
		return leaveReturnDate;
	}

	public void setLeaveReturnDate(String leaveReturnDate) {
		this.leaveReturnDate = leaveReturnDate;
	}

	public String getLeaveStartDate() {
		return leaveStartDate;
	}

	public void setLeaveStartDate(String leaveStartDate) {
		this.leaveStartDate = leaveStartDate;
	}
	
	
}
