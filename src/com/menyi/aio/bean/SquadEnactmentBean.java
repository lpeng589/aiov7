package com.menyi.aio.bean;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

public class SquadEnactmentBean implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private String squadEnactmentNo;
	private List<SquadSect> squadList = new ArrayList<SquadSect>();
	
	public SquadEnactmentBean() {}
	
	public SquadEnactmentBean(String squadEnactmentNo,List<SquadSect> squadList){
		this.squadEnactmentNo = squadEnactmentNo;
		this.squadList = squadList;
	}

	public static long getSerialVersionUID() {
		return serialVersionUID;
	}

	public String getSquadEnactmentNo() {
		return squadEnactmentNo;
	}

	public void setSquadEnactmentNo(String squadEnactmentNo) {
		this.squadEnactmentNo = squadEnactmentNo;
	}

	public List<SquadSect> getSquadList() {
		return squadList;
	}

	public void setSquadList(List<SquadSect> squadList) {
		this.squadList = squadList;
	}
	
	
}
