public class ImportState {
    private int total;
    private int success;
    private int fail;
    private boolean cancel = false;
    public ImportState(int total,int success,int fail) {
        this.total = total;
        this.success = success;
        this.fail = fail;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public void setSuccess(int success) {
        this.success = success;
    }

    public void setFail(int fail) {
        this.fail = fail;
    }

    public void setCancel(boolean cancel) {
        this.cancel = cancel;
    }

    public int getTotal() {
        return total;
    }

    public int getSuccess() {
        return success;
    }

    public int getFail() {
        return fail;
    }

    public boolean isCancel() {
        return cancel;
    }


}
