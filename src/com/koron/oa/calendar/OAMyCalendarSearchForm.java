package com.koron.oa.calendar;

import com.menyi.web.util.BaseSearchForm;

public class OAMyCalendarSearchForm extends BaseSearchForm{
	
	
	private String calendarDate;
	private String endDate;
	private String calendarTitle;
	private String calendarContext;
	private String keyWord;
	
	public String getCalendarContext() {
		return calendarContext;
	}
	public void setCalendarContext(String calendarContext) {
		this.calendarContext = calendarContext;
	}
	public String getCalendarDate() {
		return calendarDate;
	}
	public void setCalendarDate(String calendarDate) {
		this.calendarDate = calendarDate;
	}
	public String getCalendarTitle() {
		return calendarTitle;
	}
	public void setCalendarTitle(String calendarTitle) {
		this.calendarTitle = calendarTitle;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public String getKeyWord() {
		return keyWord;
	}
	public void setKeyWord(String keyWord) {
		this.keyWord = keyWord;
	}
	
	
	
}
