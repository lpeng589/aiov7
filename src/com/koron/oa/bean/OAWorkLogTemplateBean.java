@Entity
@Table(name = "tblPlanTemplate")
public class OAWorkLogTemplateBean {
	@Id
	private String id;// ID存放的是表名

	private String name;//名称

	private String toplanType;//week,day

	private String content;//模板内容

	private String userIds;// 职员ids

	private String deptIds;// 部门ids

	private String createBy;// 创建人

	private String createTime;// 创建时间

	private String lastUpdateBy;// 最后修改人

	private String lastUpdateTime;// 最后修改时间

	private String statusId;// 是否启用0启用，-1不启用

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getToplanType() {
		return toplanType;
	}

	public void setToplanType(String toplanType) {
		this.toplanType = toplanType;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getUserIds() {
		return userIds;
	}

	public void setUserIds(String userIds) {
		this.userIds = userIds;
	}

	public String getDeptIds() {
		return deptIds;
	}

	public void setDeptIds(String deptIds) {
		this.deptIds = deptIds;
	}

	public String getCreateBy() {
		return createBy;
	}

	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getLastUpdateBy() {
		return lastUpdateBy;
	}

	public void setLastUpdateBy(String lastUpdateBy) {
		this.lastUpdateBy = lastUpdateBy;
	}

	public String getLastUpdateTime() {
		return lastUpdateTime;
	}

	public void setLastUpdateTime(String lastUpdateTime) {
		this.lastUpdateTime = lastUpdateTime;
	}

	public String getStatusId() {
		return statusId;
	}

	public void setStatusId(String statusId) {
		this.statusId = statusId;
	}

	
}
